print("=====================================猜数游戏======================================")
import random
num = int(random.randint(1, 100))
count = 0
money = 5000
print("             -----------------------------------------------------------")
while money - 1000 >= 0:
    count = count + 1
    guess = int(input("                             请输入要猜的数字："))
    print("             -----------------------------------------------------------")
    if guess > num:
        money = money - 1000
        print("                             提示：大了,余额：", money, "次数：", count)
        print("             -----------------------------------------------------------")
    elif guess < num:
        money = money - 1000
        print("                             提示：小了,余额：", money, "次数：", count)
        print("             -----------------------------------------------------------")
    else:
        money = money - 1000
        if count <= 3:
            money = money + 200
        else:
            money = money + 150
        print("                             恭喜您!猜对了，一共猜了", count, "次", "余额还有", money)
        print("             -----------------------------------------------------------")
        break
if money == 0 or money < 1000:
    print("                             很遗憾！金币已用完，请充值再战")
    print("             -----------------------------------------------------------")








